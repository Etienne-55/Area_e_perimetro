print("Perímetro e área do triângulo.\n")

LA = int(input("Informe o lado do primeiro lado: "))

LB = int(input("Iforme o lado do segundo lado: "))

LC = int(input("Informe o valor do terceiro lado: "))

ALT = int(input("Informe a altura: "))

B = int(input("Informe o valor da base: "))

print("Perímetro =", LA + LB + LC) 
print("Área =", (B * ALT) / 2)